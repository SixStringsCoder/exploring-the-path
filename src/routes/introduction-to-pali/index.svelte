<h1>Introduction to Pali Course</h1>

<main>
  <p>Welcome to the Introduction to Pāli section of the Pariyatti Learning Center. Below, you will find a number of learning tools to help you become familiar with the grammar, pronunciation and declensions used in the Pāli language. This section is intended to provide a background as well as a continual reference for students that are working through the other sections of the Pariyatti Learning Center such as <a title="Exploring the Path" target="_blank" href="https://learning.pariyatti.org/course/view.php?id=2">Exploring the Path</a>.</p>
   <p>If you are new to the study of Pāli, we recommend that you read through the document below: Introduction to Pāli Grammar. This is a handy reference document for when you are working on translations from Pāli to English or vice versa. The other tools below are intended to help increase your Pāli vocabulary and to provide fun and engaging learning tools.</p>
  
  <p>To enter in the special Pāli characters using your computer keyboard, you'll need to download and install the <a href="https://www.tipitaka.org/keyboard/" target="_blank">Pāli Keyboard</a>.</p>
</main>


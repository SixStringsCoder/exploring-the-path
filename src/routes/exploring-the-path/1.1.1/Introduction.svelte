<section class="intro-content-container">
  <p class="indent">The reknown meditation teacher S.N. Goenka labelled ‘pariyatti’, the study of the words of the Buddha, the original Pāli-suttas the ‘gold’ that embellishes the ‘gem’ of ‘paṭipatti’, the beneficial practise of meditation. In this sense all quotes, suttas and selections of pariyatti in this ‘Exploring the Path in the Direct Words of the Buddha’<sup>2</sup> along with the introduction and voaculary training intend to provide inspiration to practise. It is ONLY application of paṭipatti which in the end will provide all real, concrete and palpable benefits for oneself, with pariyatti rendering encouragement, motivation and thrill.</p>

<p class="indent">For providing thrill the first chapter of this collection endeavours to point out the rarity of something that seems far too natural:</p> 
  
<blockquote class="justified-indented-quote">
<p><strong><em>The good fortune for human beings in the present period of existence being able to encounter the magnificent, result oriented and beneficial teaching of the Enlightened One!</strong></em></p>
<p>While aeons after aeons do pass in darkness, mankind currently encounters a remedy not only on the level of theoretical understanding and philosophical satisfaction but also as real and applicable practise through the ‘gem’ that is so generously presented in Vipassana<sup>3</sup></p>.
</blockquote> 
  
<blockquote class="justified-indented-quote">What is the use if I alone should cross, a man aware of my possibilities? Once I will have reached omniscience I will help all including devas to cross to the other shore. 
</blockquote>  

<p>After the Buddha had ordained his first five disciples, the noble youth Yasa and his friends, the number of Arahants had grown up to sixty-one. Before the first rains retreat the Buddha stirred them with the following words to spread the Dhamma:</p>
<p class="pali">‘Caratha, bhikkhave, cārikaṃ bahujanahitāya bahujanasukhāya lokānukampāya atthāya hitāya sukhāya devamanussānaṃ; mā ekena dve agamittha; desetha, bhikkhave, dhammaṃ ādikalyāṇaṃ majjhekalyāṇaṃ pariyosānakalyāṇaṃ sātthaṃ sabyañjanaṃ kevalaparipuṇṇaṃ<sup>4</sup>  parisuddhaṃ brahmacariyaṃ pakāsetha.’<sup>5</sup></p> 
<p>‘Wander forth, Bhikkhus, for the wellbeing of many, for the happiness of many, out of compassion for the world, for the benefit, the welfare and happiness of gods and men. No two should wander together the same direction. Proclaim, Bhikkhus, the Dhamma which is beneficial in the beginning, beneficial in the middle, and beneficial in the end, with correct wording full of meaning, complete in itself, utterly pure and displaying the holy life.
</p> 
  
<p>
  The opening text of this Pāli-learning-programme, the Bahujanahitasutta is selected from the Itivuttakapāḷi, a collection of all suttas that begin with ‘vuttañhetaṃ’—‘this has been said’.<sup>6</sup> It is the fifth sutta from the chapter under the title —‘tikanipāto’— ‘things with three’ The three things described in this sutta are the three individuals who arise for the benefit and welfare of all beings, —‘bahujanahitaya’— the Buddha, the Tathagata<sup>7</sup>, an Arahant and a disciple, able to teach the Dhamma.</p>
<p>‘Vuttañhetaṃ’—‘this has been said’—refers to the female slave of Queen Samāvāti by the name Khujjhuttarā in Kosambī. The name Khujjhuttarā was given to her because she had been born with a deformed back (khujja—bent). It is said that in one of her past lives she had imitated a Pacceka Buddha, who was a hunchback. Despite her position as a slave-girl (because in another previous life she had treated a nun like her servant-girl) she had the capacity of quick understanding and the ability to memorize whatever she heard. This was a result of a situation when she proved fast thinking in a previous life, where she quickly presented bracelets to Pacceka Buddhas, who had trouble holding extremely hot food in their bowls.</p>
<p>Her main duty under queen Samāvāti had been to procure a fresh bouquet of flowers everyday. It had been her habit to buy only half a bunch of flowers and to keep the other half of money for herself. This attitude changed when she listened to a discourse of the Buddha one day, which inspired her to present the correct amount of flowers to the queen. The queen was surprised by the large amount of beautiful flowers on that day and was accordingly informed about the past bad habit. But instead of rebuking Kujjhuttarā, the Queen Samāvāti instructed her with a new task: From hence on she had to go and listen to the discourses of the Buddha and to repeat them to her every day.
</p>
  
<p>When the inherent qualities of Dhamma mature within one who treads the path as described by the Buddha then the value of ehipassiko<sup>8</sup> usually manifests within. One develops the zeal of sharing with many the benefits —‘bahujanahitaya’— and merits one has gained oneself as a result of applying the Dhamma.</p>  
<p>The sole desire to help other beings has been the arduous dedication of the Buddha that he fostered during an unthinkable period of four asaṅkheyye<sup>9</sup> and hundred thousand kappas<sup>10</sup>: 
</p>
  
<blockquote class="justified-indented-quote">
<span class="pali">Kappe ca satasahasse<sup>11</sup>, caturo ca asaṅkhiya; 
amaraṃ nāma nagaraṃ, dassaneyyaṃ manoramaṃ<sup>12</sup>.  
</span>.
</blockquote> 
  
<blockquote class="justified-indented-quote">Hundred thousand aeons and four incalculabels ago there was a delightful city by the name of Amara<sup>13</sup>, pleasant to the eye.  
</blockquote>
  
<p>This desire to help other beings caused the Buddha, then as Samaṇa Sumedha, to prostate at the feet of Buddha Dīpankara and to express his aspiration to become a Sammasambuddha himself. This desire to help other beings had been the singular reason to relinquish the possible enlightenment for himself with the thought:</p>
  
<blockquote class="justified-indented-quote">
<span class="pali">‘Kiṃ me ekena tiṇṇena<sup>14</sup>, purisena thāmadassinā<sup>15</sup>; 
sabbaññutaṃ pāpuṇitvā, santāressaṃ<sup>16</sup> sadevakaṃ.’<sup>17</sup>  
</blockquote> 
  
<blockquote class="justified-indented-quote">‘What is the use if I alone should cross, a man seeing my powers? Once I will have reached omniscience I will help all beings including devas to cross to the other shore.’  
</blockquote>  

<p>It was his desire to help and enable other beings to realise the ultimate benefit, which is nibbāna: Tattha atthāyāti iminā paramatthāya<sup>18</sup>, nibbānāyāti vuttaṃ hoti.<sup>19</sup></p>  
<p>It was his desire to help other beings to present the path that, once applied enables one to reach the highest wellbeing, which is nibbāna: 
</p> 
<blockquote class="justified-indented-quote pali">
Hitāyāti taṃsampāpakamaggatthāyāti<sup>20</sup> vuttaṃ hoti.  Nibbānasampāpakamaggato  hi uttariṃ hitaṃ nāmanatthi.  
</blockquote>   

<p>It was his desire to help and enable other beings to realise that very happiness through the successful attainment of entering the stream towards nibbāna:</p>

<blockquote class="justified-indented-quote pali">
Sukhāyāti  phalasamāpattisukhatthāyāti<sup>21</sup> vuttaṃ  hoti, <br>
tato uttari sukhābhāvato.  
</blockquote>   

<p>All this zeal of sharing one’s merits develops as the result of one’s own walking on the path of Dhamma, which in itself is beneficial, supportive and leading to welfarein three ways:</p>
  
<blockquote class="justified-indented-quote pali">
‘ādikalyāṇaṃ majjhekalyāṇaṃ  pariyosānakalyāṇaṃ’   
</blockquote>   

<blockquote class="justified-indented-quote">
‘beneficial from the very beginning, in the middle and beneficial all through to the very end’    
</blockquote> 
  
<p>One starts realizing that enormous benefits are already derived by starting to observe the moral principles of sīla, these benefits multiply once one starts practicing the calmimg meditation of Vipassana and reach their peak in realizing the fruit of nibbāna:</p>  
  
<blockquote class="justified-indented-quote pali">
 … ‘sāsanadhammo attano  atthabhūtena  sīlena  ādikalyāṇo,  samathavipassanāmaggaphalehi majjhekalyāṇo, nibbānena pariyosānakalyāṇ’  
</blockquote> 
 
<p>One tastes the beauty of Dhamma from the very beginning through sīla and samādhi, in the middle through Vipassana and the path, in the end by realizing the fruit: </p>  
 
<blockquote class="justified-indented-quote pali">
… ‘sīlasamādhīhi vā ādikalyāṇo, vipassanāmaggehi  majjhekalyāṇo, phalanibbānehi pariyosānakalyāṇo’   
</blockquote> 
  
<p>The beauty of Dhamma presents itself in the truly enlightened Buddha, in a Dhamma that is pure, and a Saṅgha that practices uprightly: </p>  
  
<blockquote class="justified-indented-quote pali">
… ‘buddhasubuddhatāya vā ādikalyāṇo,   dhammasudhammatāya   majjhekalyāṇo,   saṅghasuppaṭipattiyā   pariyosānakalyāṇo’  
</blockquote>  
  
<p>May this first selected sutta, the Bahujanahitasuttaṃ inspire many beings to walk on the path, realize its benefits that are: — ‘ādikalyāṇaṃ majjhekalyāṇaṃ  pariyosānakalyāṇaṃ’ — and may these be shared with many other beings!</p>  
  

  <h2 class="intro-number center">Introduction to Lesson 1.1.1</h2>
  <h3 class="intro-title center">Bahujanahitasuttaṃ (For the Benefit of Many)</h3>
</header>

<section>
  <pre class="pali center-text">
  Jāge jāge dharama kī vāṇī
  Maṅgala mūla mahā kalyānī
  Maṅgala mūla mahā kalyānī
  Jāge jāge dharama kī vāṇī
  </pre>

  <pre class="center-text">
  May the words of the Dhamma arise,
  Those roots of great happiness and wellbeing,
  Those roots of great happiness and wellbeing,

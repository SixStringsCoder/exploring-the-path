  <p><a href="#pali-note1"><span id="pali-word1">Vuttañhetaṃ</span><sup>1</sup></a> bhagavatā, vuttamarahatāti me sutaṃ–</p>
  <blockquote class="justified-indented-quote">
   <p>“Tayome puggalā loke uppajjamānā<sup>2</sup> uppajjanti bahujanahitāya bahujanasukhāya<sup>3</sup> lokānukampāya<sup>4</sup> atthāya hitāya sukhāya devamanussānaṃ. Katame tayo? Idha, bhikkhave<sup>5</sup>, <sup>6</sup> loke uppajjati<sup>7</sup> arahaṃ<sup>8</sup> sammāsambuddho vijjācaraṇasampanno<sup>9</sup> sugato<sup>10</sup> lokavidū anuttaro purisadammasārathi satthā devamanussānaṃ buddho
bhagavā.<sup>11</sup> So dhammaṃ deseti ādikalyāṇaṃ majjhekalyāṇaṃ pariyosānakalyāṇaṃ sātthaṃ sabyañjanaṃ, kevalaparipuṇṇaṃ parisuddhaṃ brahmacariyaṃ<sup>12</sup> pakāseti. Ayaṃ, bhikkhave, paṭhamo puggalo loke uppajjamāno uppajjati bahujanahitāya bahujanasukhāya lokānukampāya atthāya hitāya sukhāya devamanussānaṃ.</p>
    <p>“Puna caparaṃ, bhikkhave, tasseva satthu sāvako arahaṃ hoti khīṇāsavo<sup>13</sup> vusitavā katakaraṇīyo ohitabhāro anuppattasadattho parikkhīṇabhavasaṃyojano sammadaññā vimutto. So dhammaṃ deseti ādikalyāṇaṃ majjhekalyāṇaṃ pariyosānakalyāṇaṃ sātthaṃ sabyañjanaṃ,<sup>14</sup> kevalaparipuṇṇaṃ parisuddhaṃ brahmacariyaṃ pakāseti. Ayaṃ, bhikkhave, dutiyo puggalo loke uppajjamāno uppajjati bahujanahitāya bahujanasukhāya lokānukampāya atthāya hitāya sukhāya devamanussānaṃ.</p> 
  </blockquote>  
  
